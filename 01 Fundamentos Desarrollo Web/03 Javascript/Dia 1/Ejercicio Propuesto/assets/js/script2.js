document.write("<h2>La resta es: "+ (nrouno - nrodos) +"</h2>")

document.write("<h3>La multiplicacion es: "+ (nrouno * nrodos) +"</h3>")

document.write("<h4>La division es: "+ (nrouno / nrodos) +"</h4>")

