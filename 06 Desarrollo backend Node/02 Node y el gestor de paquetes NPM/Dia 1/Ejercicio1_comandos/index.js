// finalmente se crea el archivo package.json
//---------------------------------------------------------------------------------------------
// crear archivo package.json de una vez:
// npm init -y
//---------------------------------------------------------------------------------------------
// instala dependencia de jquery
// npm i jquery
//---------------------------------------------------------------------------------------------
// desinstala dependencia de jquery
// npm uninstall jquery
//---------------------------------------------------------------------------------------------
// instalar todas las dependencias contenidas en el package.json
// npm i
//---------------------------------------------------------------------------------------------
// actualizar dependencias:
// npm update
// Nota: este comando no funciona del todo bien, no se recomiendo usarlo
//---------------------------------------------------------------------------------------------
// instalar version especifica de una dependencia:
// npm uninstall jquery
// npm i jquery@3.5.0
//---------------------------------------------------------------------------------------------
// instalara nodemon para el proyecto especifico
// npm i nodemon
// Nota: esta dependencia es de producion, no de desarrollo, por lo que se recomienda
// instalarlo en forma global, no solo para un proyecto.
//---------------------------------------------------------------------------------------------
// instalar nodemon de forma global:
// npm install -g nodemon
//---------------------------------------------------------------------------------------------
// USAR NODEMON:
// npm init -y
// npm i nodemon
// nodemon index.js
